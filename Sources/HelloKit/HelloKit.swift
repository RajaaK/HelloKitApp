public struct HelloKit {
    
    public init() {
    }
    
    public var text = "Hello, World!"
    
    public func addNumbers(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func subNumbers(a: Int, b: Int) -> Int {
        return a - b
    }
    public func mulNumbers(a: Int, b: Int) -> Int {
        return a * b
    }
    public func divNumbers(a: Int, b: Int) -> Int {
        return a / b
    }
}
